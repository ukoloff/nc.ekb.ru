<H1>OpenVPN</H1>

Сервер <A hRef='http://openvpn.net'>OpenVPN</A> запущен в тестовом режиме.
Он позволяет авторизованным пользователям, находясь в любой точке земного шара
(где имеется доступ в Интернет) подключиться к локальной сети ОАО "Уралхиммаш" и получить доступ
ко всем внутренним ресурсам.

<H2>Установка клиента OpenVPN</H2>
<LI>Скачайте стандартный <A hRef='openvpn-2.1.4-install.exe'>дистрибутив OpenVPN</A>
<LI>Запустите и установите его с настройками по умолчанию (требуются права локального администратора)
<LI>Если во время установки появится предупреждение об установке неподписанного драйвера - соглашайтесь
<LI>Скачайте файл <A hRef='config.zip'>настроек Уралхиммаш</A> и распакуйте его в папку программы
(обычно это C:\Program Files\OpenVPN\config)
<LI>Вероятно, есть смысл перенести ярлык OpenVPN GUI с рабочего стола в папку автозагрузки
(для текущего пользователя или для всех пользователей)
<LI>Уговорите администратора добавить Вашу учётную запись в доменную группу OpenVPN
<LI>Если Вы находитесь за фаерволом (внутри локальной сети другого предприятия), то нужно уговорить
администратора фаервола открыть доступ к ekb.ru по порту 1194 (официальному порту OpenVPN)
<LI>Если планируется запуск OpenVPN от имени пользователя, не имеющего прав локального администратора,
дополнительно выполните:
<UL>
<LI>Включите его в локальную группу "Операторы настройки сети"/'Network Configuration Operators'
<LI>Дайте этому пользователю (или всем пользователям) права на запись в папку журнала OpenVPN
(Обычно это C:\Program Files\OpenVPN\log)
<LI>Запустите один раз OpenVPN GUI от имени администратора
</UL>

