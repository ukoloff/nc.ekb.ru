<A Name='general'><H2>Общие положения</H2></A>
Доступ в глобальную вычислительную сеть Internet через локальную сеть ОАО "Уралхиммаш"
может быть предоставлен сотрудникам ОАО "Уралхиммаш", а также сотрудникам других организаций,
находящимся на территории ОАО "Уралхиммаш" по договору с этими организациями.
<P />
Доступ в Internet предоставляется конкретным физическим лицам. Каждому, имеющему доступ в Internet,
предоставляется учётная запись и пароль. Любой владелец учётной записи несёт полную ответственность
за сохранность и секретность своего пароля. Весь трафик Internet, полученный под его учётной записью,
будет безусловно записан на его личный счёт, и он также будет нести полную ответственность за любые
противоправные действия, осущёствлённые под его учётной записью.
<P />
Весь доступ в Интернет протоколируется и может при необходимости быть предоставлен в детальном отчёте.
<P />
Для каждого пользователя регулярно подсчитывается его суммарный трафик с начала месяца.
Этот трафик отображается на <A hRef='https://ekb.ru/stat/'>странице статистики</A> пользователя
и служит основанием для начисления платы за использование Internet и автоматического отключения.
<P />
