<H2>Технические подробности</H2>
Информация о технических вопросах подключения к глобальной вычислительной сети Internet
через локальную сеть ОАО "Уралхиммаш" приведена <A hRef='../#proxy'>на этой странице</A>.
<P />
Собственно процедуру подключения (если доступ уже предоставлен)
и  консультации по техническим вопросам осуществляет
<A hRef='mailto:911@ekb.ru'>служба технической поддержки</A>, тел. 78-50.
<P />
Сведения о текущем объёме трафике и доступ к управлению учётной записью постоянно доступен
через сайт <A hRef='https://ekb.ru/'>Сетевого центра</A>, и в частности страницу
<A hRef='https://ekb.ru/stat/'>статистики</A> и
<A hRef='https://ekb.ru/pass/'>страницу смены персонального пароля</A>.
<P />
Руководители подразделений и организаций, чьи сотрудники имеют доступ в Internet, могут обратиться
в Отдел Информационных Технологий, чтобы просматривать на странице статистики не только свою личную
статистику, но и статистику своих подчинённых.

<A Name='price'><H2>Стоимость трафика</H2></A>
Весь трафик, превышающий бесплатный месячный порог (устанавливаемый индивидуально
для каждого пользователя), оплачивается (независимо от вида трафика) по 2.5 рубля за каждый мегабайт
входящего трафика.
<P />
Плата взимается:
<UL>
<LI>Для сотрудников ОАО "Уралхиммаш" путём удержания из заработной платы
<LI>Для сотрудников других организаций - как указано в договоре между ОАО "Уралхиммаш" и этими организациями
</UL>
<A Name='docs'><H2>Образцы документов</H2></A>
Предоставление доступа в Интернет осуществляется по соответствующей служебной записке начальника подразделения.
Образец такой служебной записки приведён <A hRef='on/'>здесь</A>.
<P />
Если пользователю уже предоставлен доступ, а он хочет получить также платный доступ (при превышении
месячного бесплатного трафика), ему необходимо однократно обратиться с
<A hRef='payed/'>соответствующим заявлением</A>, завизированным руководителем подразделения.
<P />
Если доступ уже предоставлен, пользователь может в любой момент изменить свой порог жёсткого отключения,
обратившись в Отдел Информационных Технологий с <A hRef='quota/'>соответствующим заявлением</A>. Порог жёсткого
отключения может быть установлен любым, в том числе и меньшим, чем бесплатный порог
(хотя последнее вряд ли имеет смысл).
