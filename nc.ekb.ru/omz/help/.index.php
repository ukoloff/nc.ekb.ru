<A Name='disk'><H2>Пересылка больших файлов</H2></A>
Почтовая система ОАО &laquo;Уралхиммаш&raquo; ограничивает размер файлов, которые могут быть пересланы по электронной почте.
Рекомендуется не прикреплять крупные (скажем, более 1 Мб) файлы к письмам,
а воспользоваться одним из множества бесплатных сервисов для временного хранения файла,
а адресату отсылать просто ссылку.
<P>
В тестовом режиме запущен сервис <A hRef='http://disk.ekb.ru'>disk.ekb.ru</A>, который предоставляет простейший сервис
такого вида как для всех работников завода (для отправки файлов во внешний мир), так и для отправки файлов извне работникам завода.
