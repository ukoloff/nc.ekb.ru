<A Name=proxy><H2>Интернет</H2></A>
Доступ к просмотру веб-страниц в сети ОАО "Уралхиммаш" осуществляется только и исключительно через
прокси сервер по адресу <Code>proxy.uxm</Code>, номер порта 3128. В некоторых программах адрес прокси-сервера
нужно указывать так: <Code>proxy.uxm:3128</Code> или даже <Code>http://proxy.uxm:3128</Code>.
В большинстве случаев вместо <Code>proxy.uxm</Code> можно писать просто <Code>proxy</Code>
<P>
Прокси для доступа в Интернет требует ввода учётной записи сети ОАО "Уралхиммаш" и пароля к ней. Доступ к прокси-серверу
возможен только из локальной сети, если Вы подключаетесь через другого провайдера, Вам нужно воспользоваться
для навигации по Интернет способом, предлагаемым провайдером.
<P>
В сети ОАО "Уралхиммаш" настроена автоматическая настройка современных браузеров на прокси-сервер. Но если она
не работает или Вы не хотите ею пользоваться, Вы можете сделать эту настройку вручную:
<UL>
<LI>Для Microsoft <A hRef='inet/proxy/'>Internet Explorer</A>
<LI>Для браузера Opera: зайдите в меню "<Code>Файл/Настройки</Code>" на страницу "<Code>Сеть</Code>" и нажмите кнопку "<Code>Прокси-серверы</Code>"
<LI>Для браузеров Netscape/Mozilla: Меню "<Code>Edit/Preferences</Code>" страница "<Code>Advanced/Proxy</Code>"
</UL>

<A Name=ntp><H2>Точное время</H2></A>
Сервер <Code>ekb.ru</Code> поддерживает протокол NTP, предназначенный для синхронизации времени в Интернет
с атомными часами, расположенными по всему земному шару. Вы можете синхронизировать часы на своём компьютере при
помощи любого клиента NTP, указав ему сервер <Code>ntp.ekb.ru</Code>.
<P>
В операционных системах Microsoft Windows 2000 и Microsoft Windows XP клиент NTP уже встроен - можно пользоваться им.
Настроить его на сервер <Code>ntp.ekb.ru</Code> проще всего выполнив <A hRef=ntp.reg>скрипт настройки NTP</A>
и включив службу Windows Time.

<A Name=net><H2>Сетевой центр</H2></A>
Сетевой центр по адресу <A hRef='https://ekb.ru/omz/'>https://ekb.ru/omz/</A> доступен как из локальной сети, так и при доступе
в Интернет через любого провайдера.
<P>
Зайдя в <A hRef='https://ekb.ru/omz/?login'>Сетевой центр</A> и введя учётную запись и пароль, Вы получаете доступ к управлению своей
учётной записью:
<UL>
<LI>Можете в любой момент самостоятельно поменять свой пароль. Единый пароль служит Вам для доступа
к электронной почте (чтению и отправке), просмотру Интернет, просмотру статистики и всем другим сервисам.
Если Вы хотя бы подозреваете, что Ваш пароль известен кому-либо кроме Вам, немедленно поменяйте его. Также
рекомендуем менять пароль регулярно, с целью профилактики ;-)
<LI>Можете отправлять и получать электронную почту без использования почтовой программы, только при помощи
обозревателя Интернет (<A hRef='https://mail.ekb.ru'>webmail</A>)
<LI>Можете просматривать статистику своего доступа к Интернет (объём скачанной информации)<!-- и
электронной почты (количество и объём полученных писем)--><!-- и модемного доступа (количество и время соединений) -->.
<LI>А также многое другое, в зависимости от предоставленных Вам полномочий
</UL>

<A Name='wsus'><H2>Windows Update</H2></A>
В локальной сети ОАО "Уралхиммаш" действует сервер, содержащий все обновления для Microsoft Windows, Microsoft Office 
и другого программного обеспечения Microsoft.
<P>
Если Ваш компьютер включён в домен, обновления будут настроены автоматически.
Если Ваш компьютер пока не в домене, Вы можете настроить его на получение обновлений, выполнив <A hRef='wsus.reg'>этот скрипт</A>.

