<?#Настройка безопасности Outlook [Express]?>

<Div Style='border: 1px dotted black; background: yellow;'>
<b>Внимание!</b>
Настройка делается таким образом для Outlook Express. Для Outlook (начиная с версии 2003)
рекомендуется подключение по протоколу <A hRef='../mapi'>MAPI</A>
</Div>

<OL>
<LI><Img Src=oelogins.gif Align=Right>Выберите пункт меню "<Code>Сервис/Учётные записи</Code>"
<LI>Если почтовый ящик ещё вообще не создан, нажмите кнопку "Добавить" и выберите пункт меню "Почта".
Далее действуйте согласно запросам программы
<LI>На вкладке "Почта" выберите учётную запись, подлежащую настройке
<LI>Нажмите кнопку "Свойства"
<BR Clear=Right>
<LI><Img Src=oembox.gif Align=Right>Выберите вкладку "Серверы"
<LI>Убедитесь, что правильно указаны:
<UL>
<LI>Сервер входящей почты <code>mail.ekb.ru</code>
<LI>Сервер исходящей почты <code>mail.ekb.ru</code>
<LI>Учётная запись
<LI>Пароль
</UL>
<LI>Проверьте, что не отмечен флажок "Использовать безопасную проверку пароля (SPA)"
<LI>Отметьте флажок "Проверка подлинности пользователя"
<BR Clear=Right>
<LI><img Src=oesmtp.gif Align=Right>Нажмите кнопку "Настройка"
<LI>Убедитесь, что выбрано "Как на сервер входящей почты" - использовать одну учётную запись для отправки и для чтения почты
<LI>Нажмите "OK"
<BR Clear=Right>
<LI><Img Src=oessl.gif ALign=Right>Выберите вкладку "Дополнительно"
<LI>Отметьте оба флажка "Подключаться через безопасное соединение"
<LI>Убедитесь, что правильно указаны оба порта, как для входящей, так и для исходящей почты
<BR Clear=Right>
<LI>Для того, чтобы при соединении с сервером Outlook [Express] не задавал глупых вопросов, рекомендуется установить
<A hRef=/ssl/omz.cer>сертификат сервера</A>. Для этого щёлкните по ссылке, выберите "Открыть" и нажмите кнопку
"Установить сертификат".

</OL>
